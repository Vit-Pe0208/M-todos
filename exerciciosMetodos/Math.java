package exerciciosMetodos;

public class Math {
	public static void main(String[] args) {
		//abs(x) valor absoluto x
		System.out.println(java.lang.Math.abs(23.7));
		System.out.println(java.lang.Math.abs(0.0));
		System.out.println(java.lang.Math.abs(-23.7));
		System.out.println();
		
		//ceil(x) arredonda x para o menor inteiro não menor que x
		System.out.println(java.lang.Math.ceil(9.2));
		System.out.println(java.lang.Math.ceil(-9.8));
		System.out.println();
		
		//cons(x)cosseno trigonométrico de x(x em radianos)
		System.out.println(java.lang.Math.cos(0.0));
		System.out.println();
		
		//exp(x) método exponencial e elevado a x
		System.out.println(java.lang.Math.exp(1.0));
		System.out.println(java.lang.Math.exp(2.0));
		System.out.println();
		
		//floor(x) arrendonda x para maior inteiro não maior que x
		
		System.out.println(java.lang.Math.floor(9.2));
		System.out.println(java.lang.Math.floor(-9.8));
		System.out.println();
		//log(x) logaritmo natural de x(base e)
         System.out.println(java.lang.Math.log(java.lang.Math.E));
         System.out.println(java.lang.Math.log(java.lang.Math.E*java.lang.Math.E));
         System.out.println();
         //max(x,y) maior valor de x e y
         System.out.println(java.lang.Math.max(2.3, 12.7));
         System.out.println(java.lang.Math.max(-2.3, -12.7));
         System.out.println();
         //min(x,y) menor valor de x e y
         System.out.println(java.lang.Math.min(2.3, 12.7));
         System.out.println(java.lang.Math.min(-2.3, -12.7));
         System.out.println();
         //pow(x,y)x elevado à potência de y
         System.out.println(java.lang.Math.pow(2.0, 7.0));
         System.out.println(java.lang.Math.pow(9.0, 0.5));
         System.out.println();
         //sin(x)seno trigonométrico de x (em radianos)
         System.out.println(java.lang.Math.sin(0.0));
         System.out.println();
         //sqrt(x)raiz quadrada de x
         System.out.println(java.lang.Math.sqrt(900.0));
         System.out.println();
         //tan(x)tangente trigonométrica de x(x em radianos)
         System.out.println(java.lang.Math.tan(0.0));
         System.out.println();
		
		
	}

}
